//============================================================================
// setup_dac()
//============================================================================
void setup_dac(void) {

    RCC -> AHBENR |= RCC_AHBENR_GPIOAEN;
    GPIOA -> MODER |= 0x0300;                       //analog operation for pin4 (GPIOA)
    RCC -> APB1ENR |= RCC_APB1ENR_DACEN;            //enables rcc clock for DAC
    DAC -> CR &= ~DAC_CR_TSEL1;                     // clears TSEL1, (timer6 TRGO event)  
    DAC -> CR |= DAC_CR_TEN1;                       // DAC channel1 trigger enable
    DAC -> CR |= DAC_CR_EN1;                        // enables DAC

}



//============================================================================
// Timer 6 ISR
//============================================================================
// Write the Timer 6 ISR here.  Be sure to give it the right name.

void TIM6_DAC_IRQHandler(void) {
    TIM6 -> SR &= ~TIM_SR_UIF;
    if (current_sfx.id != NO_SID) {
        return;
    } 
    offset0 += step0;
    offset1 += step1;
    if (offset0 >= (N << 16)) {
        offset0 -= (N << 16);
    }
    if (offset1 >= (N << 16)) {
        offset1 -= (N << 16);
    }

    int samp = current_sfx.sound_wave[offset0 >> 16] + current_sfx.sound_wave[offset1 >> 16];
    samp *= volume;
    samp = samp >> 17;
    samp += 2048;
    DAC -> DHR12R1 = samp;

}

//============================================================================
// init_tim6()
//============================================================================
void init_tim6(void) {

    RCC -> APB1ENR |= RCC_APB1ENR_TIM6EN;
    TIM6 -> PSC = (1000000 / RATE) - 1;
    TIM6 -> ARR = 47;
    TIM6->CR2 |= 0x0020;
    TIM6 -> DIER |= 0x1;
    NVIC_EnableIRQ(TIM6_IRQn);
    TIM6 -> CR1 |= TIM_CR1_CEN;
        
}

